# El modulo "sys"

#Es un modulo estandar que proporciona acceso a variables y funciones relacionadas con la funcionalidad del intérprete de Python
#y el sistema en el que se está ejecutando el programa. 

#se utiliza principalmente para realizar operaciones específicas del sistema, como la manipulación de argumentos de línea de comandos
#la interacción con el intérprete y la configuración del comportamiento del sistema.

#Algunos de sus usos son:

#1) Acceso a argumentos de línea de comandos
#2) Rutas y ubicaciones de búsqueda de módulos
#3) Funciones de sistema
#4) Información del intérprete
#5) Funciones de bajo nivel
#6) Depuración y manipulación del intérprete
